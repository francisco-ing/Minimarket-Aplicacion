    public void conectarUsuario(String usuario, String clave){
        try ( Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/Mercado", "root", "")) {
            String sql = "select * from usuario where usuario = ? and clave = ?";
            try ( PreparedStatement ps = con.prepareStatement(sql)) {
                ps.setString(1,usuario);
                ps.setString(2, clave);
                try ( ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                          setVisible(false);
                          jfPagina1.pack();
                          jfPagina1.setVisible(true);
                          jtfUsuario.setText("");
                          jpfClave.setText("");
                          jlbVendedor.setText(jtfUsuario.getText());
                    } else {
                        JOptionPane.showMessageDialog(this, "Error, usuario incorrecto!.");
                    }
                }
            }
        } catch (Exception e) {
            error("Error producido: " + e);
        }
    }
    
