    private void jtfCodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfCodigoKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) { 
         try ( Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/Mercado", "root", "")) {
            String sql = "select * from producto where codigo = ?";
            try ( PreparedStatement ps = con.prepareStatement(sql)) {
                ps.setString(1, jtfCodigo.getText());
                try ( ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        dtm.addRow(new Object[]{
                            rs.getString("codigo"),
                            rs.getString("nombre"),
                            rs.getInt("precio")  
                            
                        });
                        jtfCodigo.setText("");
                        contadorProducto += 1;
                        total += Integer.parseInt(rs.getString("precio"));
                        subtotal += Integer.parseInt(rs.getString("precio")) * 0.81;
                        iva += Integer.parseInt(rs.getString("precio")) * 0.18;
                        jlbProductos.setText("" + contadorProducto);
                        jlbSubtotal.setText("$" + subtotal);
                        jlbIva.setText("$" + iva);
                        jlbTotal.setText("$" + total);
                    } else {
                        JOptionPane.showMessageDialog(this, "Producto no encontrado!.");
                        jtfCodigo.setText("");
                    }
                }
            }
            } catch (Exception e) {
                error("Error producido: " + e);
            }
        }
    }//GEN-LAST:event_jtfCodigoKeyReleased

